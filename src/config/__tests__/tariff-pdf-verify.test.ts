  const zones = Object.keys(DHL_PDF);

  zones.forEach(zone => {
    describe(`DHL ${zone}`, () => {
      const weights = Object.keys(DHL_PDF[zone]).map(Number);
      weights.forEach(kg => {
        it(`${zone} @ ${kg}kg = ${DHL_PDF[zone][kg]}`, () => {
          expect(DHL_EXACT_RATES[zone]?.[kg]).toBe(DHL_PDF[zone][kg]);
        });
      });
    });
  });

  describe('DHL Range Rates', () => {
    const tier = DHL_RANGE_RATES[0];
    Object.entries(DHL_RANGE_PDF['30.1+']).forEach(([zone, rate]) => {
      it(`30.1+ ${zone} = ${rate}/kg`, () => {
        expect((tier.rates as Record<string, number>)[zone]).toBe(rate);
      });
    });
  });
